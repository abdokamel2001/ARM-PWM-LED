/**********************************************************************************************************************
 *  LOCAL DATA 
 *********************************************************************************************************************/
volatile boolean Level = HIGH;

/**********************************************************************************************************************
 *  GLOBAL DATA
 *********************************************************************************************************************/
volatile static boolean Not_Interrupted = HIGH;

/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
/******************************************************************************
* \Syntax          : void PWM()        
* \Description     : generate PWM using system timer                                   
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                               
* \Parameters (in) : None                   
* \Parameters (out): None                                                      
* \Return value:   : None                          
*******************************************************************************/
void PWM(){
    if (Duration > 1){
        STRELOAD = 0xFFFFFF;
    }else if (Duration > 0){
        STRELOAD = (uint32)((0xFFFFFF) * Duration);
    }else{
        if(Level){
            Duration = OFF_Time;
            Level = LOW;
            PWM();
        }else{
            Duration = ON_Time;
            Level = HIGH;
            PWM();
        }
    }
    SetBit(STCTRL,0);
    STCURRENT = LOW;
    if(Level){
        SetBit(PORTF_DATA,1);
    }else{
        ClrBit(PORTF_DATA,1);
    }
    while (Not_Interrupted);
    Not_Interrupted = HIGH;
    Duration--;
    PWM();
}

